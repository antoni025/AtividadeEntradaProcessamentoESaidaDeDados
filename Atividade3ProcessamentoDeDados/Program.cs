namespace Atividade3ProcessamentoDeDados
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int x = 1;
            Console.WriteLine($"Valor inicial de x é: {x}");
            Console.WriteLine($"O resultado da atribuição de x+=1 é: {x += 1}");
            Console.WriteLine($"O resultado da atribuição de x-=1 é: {x -= 1}");
            Console.WriteLine($"O resultado da atribuição de x*=1 é: {x *= 1}");
            Console.WriteLine($"O resultado da atribuição de x/=1 é: {x /= 1}");
            Console.WriteLine($"O resultado da atribuição de x%=1 é: {x /= 1}");
        }
    }
}
